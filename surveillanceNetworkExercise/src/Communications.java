
public abstract class Communications {
    protected String communicationNumber1;
    protected String communicationNumber2;
    protected int communicationYear;
    protected int communicationMonth;
    protected int communicationDay;
	
    
    public Communications(String communicationNumber1, String communicationNumber2, int communicationYear,
			int communicationMonth, int communicationDay) {
		super();
		this.communicationNumber1 = communicationNumber1;
		this.communicationNumber2 = communicationNumber2;
		this.communicationYear = communicationYear;
		this.communicationMonth = communicationMonth;
		this.communicationDay = communicationDay;
	}
	
    
    
    

    
    
}
